const Starbucks = {
  кофе: {
    американо:
      "Американо - классический напиток из одного или двух эспрессо с добавлением горячей воды.",
    латте:
      "Латте - напиток на основе эспрессо с молоком, обычно с добавлением молочной пены.",
    капуччино:
      "Капуччино - комбинация эспрессо, горячего молока и молочной пены, обычно украшенной какао.",
    экспрес: false,
    "3 в 1": false,
    "3в1": false,
    // Другие разновидности кофе и их описания
  },
  чай: {
    зеленый:
      "Зеленый чай - чай, получаемый из неокисленных листьев чайного куста.",
    черный:
      "Черный чай - полностью окисленный чай с более насыщенным ароматом и вкусом.",
    травяной:
      "Травяной чай - смесь сушеных трав, цветов, фруктов или специй, не содержащая чайного листа.",
    // Другие разновидности чая и их описания
  },
  orders: [],

  // Принятие заказа
  takeOrder: function () {
    const itemType = prompt("Введите тип напитка (кофе или чай):");
    if (itemType !== "кофе" && itemType !== "чай") {
      console.log("Извините, такого типа напитка не предлагается.");
      this.takeOrder(); // Запустить повторно при ошибке
      return;
    }

    const itemName = prompt("Введите название напитка:");
    let item;

    if (itemType === "кофе") {
      item = this.кофе[itemName.toLowerCase()];
    } else if (itemType === "чай") {
      item = this.чай[itemName.toLowerCase()];
    }

    if (item === undefined) {
      console.log("Извините, такой напиток не найден в меню.");
      this.takeOrder(); // Запустить повторно при ошибке
    } else if (item === false) {
      console.log("Извините, выберите что-то другое.");
      this.takeOrder(); // Запустить повторно при ошибке
    } else {
      console.log(item);
      this.orders.push({ тип: itemType, название: itemName });
      const proceed = confirm("Принять этот заказ?");
      if (!proceed) {
        this.orders.pop(); // Удалить последний заказ, если пользователь не хочет его принимать
      }
      const restart = confirm("Сделать еще заказ?");
      if (restart) {
        this.takeOrder(); // Запустить повторно для нового заказа
      } else {
        console.log("Спасибо за ваш заказ!");
        this.printOrders();
        this.offerMuffin();
      }
    }
  },

  // Вывод текущих заказов
  printOrders: function () {
    console.log("Текущие заказы:");
    this.orders.forEach((order, index) => {
      console.log(
        `${index + 1}. Тип: ${order.тип}, Название: ${order.название}`
      );
    });
  },

  // Предложение булочки с маком
  offerMuffin: function () {
    const answer = confirm("Хотите добавить булочку с маком к вашему заказу?");
    if (answer) {
      console.log("Булочка с маком добавлена к вашему заказу.");
    } else {
      console.log("Булочка с маком не добавлена к вашему заказу.");
    }
  },
};

